package cucerdariancatalin.tetris.game

/**
 * Enum representing different paint styles for drawing blocks on the game board.
 * - [STOKE]: Blocks are outlined.
 * - [FILL]: Blocks are filled with color.
 */
enum class PaintStyle {
    /**
     * Paint style where blocks are outlined.
     */
    STOKE,

    /**
     * Paint style where blocks are filled with color.
     */
    FILL
}